#include <libcc/crypto/aes.h>
#include <libcc/string.h>
#include <stdio.h>
#include <assert.h>

void test_aes_setkey_enc() {
    _cc_aes_t ctx;
    byte_t key[32] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
                     0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f};
    int res = _cc_aes_setkey_enc(&ctx, key, 256);
    assert(res == 0);
    printf("test_aes_setkey_enc passed\n");
}

void test_aes_encrypt_decrypt() {
    _cc_aes_t ctx;
    byte_t key[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f};
    byte_t input[16] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff};
    byte_t output[16];
    byte_t decrypted[16];

    _cc_aes_setkey_enc(&ctx, key, 128);
    _cc_aes_encrypt(&ctx, input, output);

    _cc_aes_setkey_dec(&ctx, key, 128);
    _cc_aes_decrypt(&ctx, output, decrypted);

    assert(memcmp(input, decrypted, 16) == 0);
    printf("test_aes_encrypt_decrypt passed\n");
}

void test_aes_invalid_key_length() {
    _cc_aes_t ctx;
    byte_t key[15] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e};
    int res = _cc_aes_setkey_enc(&ctx, key, 120);
    assert(res != 0);
    printf("test_aes_invalid_key_length passed\n");
}

int main() {
    test_aes_setkey_enc();
    test_aes_encrypt_decrypt();
    test_aes_invalid_key_length();
    return 0;
}